    @Override
    public boolean executeStep() {
        
            
            Jugador.Accion accion = (Jugador.Accion) this.acciones.getSelectedItem();
            Contrincante contrincante = Game.getInstance().getJugador().getContrincantes().get(this.jugadorActual);
            switch (accion) {
                case Call:
                    contrincante.setCantidadCall(contrincante.getCantidadCall() + 1);
                    break;
                case Raise:
                    contrincante.setCantidadRaise(contrincante.getCantidadRaise() + 1);
                    break;
                case Fold:
                    contrincante.setCantidadFold(contrincante.getCantidadFold() + 1);
                    break;
            }
            this.jugadorActual++;
            this.label.setText("Accion jugador " + (this.jugadorActual + 1));
        return this.jugadorActual < Game.getInstance().getJugador().getContrincantes().size() ;
    }

    @Override
    public void setVisible(boolean visible) {
        this.label.setText("Accion jugador 1");
        this.jugadorActual = 0;
        super.setVisible(visible);
    }

    private void LoadAcciones() {
        this.acciones.removeAllItems();
        for (final Accion accion : Jugador.Accion.values()) {
            this.acciones.addItem(accion);
        }
    }
