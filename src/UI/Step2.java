    @Override
    public boolean executeStep() {
        try {
            int apuesta = Integer.parseInt(this.apuestaRequerida.getText());
            int monto = Integer.parseInt(this.montoMano.getText());
            Mano manoJugador = (Mano) this.manos.getSelectedItem();
            Game.getInstance().setApuesta(apuesta);
            Game.getInstance().setMonto(monto);
            Game.getInstance().getJugador().setMano(manoJugador);
            Game.getInstance().Run();
            return false;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor num√©rico", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return true;
        }
    }

    private void LoadManos() {
        this.manos.removeAllItems();
        for (final Mano mano : Mano.values()) {
            this.manos.addItem(mano);
        }
    }
